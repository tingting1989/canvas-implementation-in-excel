/**
 * 图层 Z-Index 常量定义
 *
 * 定义所有渲染图层的 z-index 值，用于控制图层叠加顺序。
 * 值越小表示图层越靠下（先渲染），值越大表示图层越靠上（后渲染）。
 *
 * ## 图层渲染顺序（从底到顶）
 * 1. TILE (10) - 瓦片层：非冻结区域的单元格数据渲染，位于最底层
 * 2. OVERLAY (20) - 叠加层：非冻结区域的选区、合并单元格等叠加效果
 * 3. FROZEN (30) - 冻结层：冻结区域的瓦片和叠加效果
 * 4. RESIZE (40) - 调整大小层：行列宽高调整指示线（临时交互，需在数据层之上）
 * 5. HEADER (50) - 表头层：行号和列标题
 * 6. DRAG_INDICATOR (60) - 拖动指示器层：列/行拖动时的位置指示
 * 7. UI (70) - UI 层：冻结线、调试信息等界面元素
 * 8. EDITOR (80) - 编辑器层：单元格编辑框，位于最顶层
 *
 * @module constants/layerZIndex
 */
export const LAYER_Z_INDEX = {
    /** 瓦片层 - 非冻结区域的单元格数据渲染（最底层） */
    TILE: 10,

    /** 叠加层 - 非冻结区域的选区、合并单元格等效果 */
    OVERLAY: 20,

    /** 冻结层 - 冻结区域的内容 */
    FROZEN: 30,

    /** 调整大小层 - 行列宽高调整指示线（临时交互反馈，需在数据层之上） */
    RESIZE: 40,

    /** 表头层 - 行号和列标题 */
    HEADER: 50,

    /** 拖动指示器层 - 列/行拖动位置指示 */
    DRAG_INDICATOR: 60,

    /** UI 层 - 冻结线、调试信息等界面元素 */
    UI: 70,

    /** 编辑器层 - 单元格编辑框（最顶层） */
    EDITOR: 80,
};
